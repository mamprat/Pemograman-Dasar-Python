# Kalkulator Sederhana
var1 = int(input("Masukkan nilai A: "))
var2 = int(input("Masukkan Nilai B: "))
penjumlahan = var1 + var2
pengurangan = var1 - var2
pembagian = var1 / var2
perkalian = var1 * var2
pangkat = var1 ** var2
modulus = var1 % var2
print(penjumlahan)
print(pengurangan)
print(pembagian)
print(perkalian)
print(pangkat)
print(modulus)
